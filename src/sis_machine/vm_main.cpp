#include "vm_launcher.h"

// STD Inclueds
#include <iostream>
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// MAIN
//
int main(int argc, char* argv[])
{
	std::string strExecuteFilePath;
	for (int i = 0; i < argc; ++i)
	{
		strExecuteFilePath += argv[i];
	}

	sis::vm::CLauncher::Run( strExecuteFilePath );

	system( "pause" );
	return 0;
}
