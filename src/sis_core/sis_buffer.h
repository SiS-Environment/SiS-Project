/////////////////////////////////////////////////////////////
	CBuffer( uint nSize = 0 )
	{
		m_pBuffer=new char[m_uSize];
	}
	~CBuffer()
	{
		delete m_pBuffer;
	}
//	inline CBuffer( char* pBuffer, uint uSize );
public:
	inline char& operator[]( uint i )
	{
		return m_pBuffer[i];
	}
