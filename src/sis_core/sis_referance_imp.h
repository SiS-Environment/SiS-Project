template <typename T>
inline CReferance<T>::CReferance( T* pContent )
{
	if ( nullptr == pContent )
		throw -1; // TODO

	m_pContent = pContent;
}


template <typename T>
inline CReferance<T>::operator CReferance<T>&()
{
	return *m_pContent;
}


template <typename T>
inline CReferance<T>::operator CReferance<T> const&() const
{
	return *m_pContent;
}


